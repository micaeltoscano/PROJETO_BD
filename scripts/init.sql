CREATE TABLE PAGAMENTO (
    IDPAGAMENTO INT GENERATED ALWAYS AS IDENTITY,
    ID_AGENDA INT NULL,
    ID_VENDA INT NULL,
    VALOR NUMERIC(10,2) NOT NULL CHECK (VALOR > 0),

    FORMA_PAGAMENTO VARCHAR(30) NOT NULL CHECK (
        FORMA_PAGAMENTO IN ('DINHEIRO', 'CARTAO_DEBITO', 'CARTAO_CREDITO', 'PIX', 'TRANSFERENCIA')),

    DATA_PAGAMENTO TIMESTAMP NOT NULL DEFAULT NOW(),
    STATUS VARCHAR(20) DEFAULT 'APROVADO' CHECK (STATUS IN ('PENDENTE', 'APROVADO', 'RECUSADO', 'ESTORNADO')),
    
    FOREIGN KEY (ID_AGENDA) REFERENCES AGENDA(IDAGENDA),
    FOREIGN KEY (ID_VENDA) REFERENCES VENDA(IDVENDA),
    
    CONSTRAINT CHK_PAGAMENTO_ORIGEM CHECK (
        (ID_AGENDA IS NOT NULL AND ID_VENDA IS NULL) OR
        (ID_AGENDA IS NULL AND ID_VENDA IS NOT NULL)),

    CONSTRAINT CHK_VALOR_POSITIVO CHECK (VALOR > 0)
    PRIMARY KEY(IDPAGAMENTO)
);

