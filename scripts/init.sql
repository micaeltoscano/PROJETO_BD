CREATE TABLE DISPONIBILIDADE (
    IDDISPONIBILIDADE INT GENERATED ALWAYS AS IDENTITY,
    ID_FUNCIONARIO INT NOT NULL,
    DIA_SEMANA VARCHAR(10) NOT NULL,
    HORA_INICIO TIME NOT NULL,
    HORA_FIM TIME NOT NULL,

    FOREIGN KEY (ID_FUNCIONARIO) REFERENCES FUNCIONARIO(IDFUNCIONARIO),
    CONSTRAINT DISPONIBILIDADE_UNICA UNIQUE(FUNCIONARIO_ID, DIA_SEMANA, HORA_INICIO, HORA_FIM),

    PRIMARY KEY(IDDISPONIBILIDADE)
);

CREATE TABLE PRODUTO (
    IDPRODUTO INT GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
    NOME VARCHAR(100) NOT NULL UNIQUE,
    VALOR NUMERIC(10,2) NOT NULL,
    TIPO VARCHAR(50) NOT NULL,

    PRIMARY KEY(IDPRODUTO)
);

CREATE TABLE ESTOQUE (
    ID_ESTOQUE INT GENERATED ALWAYS AS IDENTITY,
    ID_PRODUTO INT NOT NULL UNIQUE,
    QUANTIDADE_ATUAL INT NOT NULL DEFAULT 0,
    QUANTIDADE_MINIMA INT NOT NULL DEFAULT 0,
    ULTIMA_ATUALIZACAO TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,

    CONSTRAINT FK_ESTOQUE_PRODUTO FOREIGN KEY (ID_PRODUTO) REFERENCES PRODUTO(IDPRODUTO),
    PRIMARY KEY(ID_ESTOQUE)
);

CREATE TABLE UTILIZA (
    IDUTILIZA INT GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
    ID_SERVICO INT NOT NULL,
    ID_PRODUTO INT NOT NULL,
    QUANTIDADE INT NOT NULL,

    FOREIGN KEY (ID_SERVICO) REFERENCES SERVICO(IDSERVICO),
    FOREIGN KEY (ID_PRODUTO) REFERENCES PRODUTO(IDPRODUTO),
    PRIMARY KEY(IDUTILIZA)
);

CREATE TABLE COMPRA (
    IDCOMPRA INT GENERATED ALWAYS AS IDENTITY,
    DATA_COMPRA TIMESTAMP NOT NULL DEFAULT NOW(),
    VALOR_TOTAL NUMERIC(10,2) NOT NULL,

    PRIMARY KEY(IDCOMPRA)
);

CREATE TABLE ITENS_COMPRA (
    IDITENSCOMPRA INT GENERATED ALWAYS AS IDENTITY,
    ID_COMPRA INT NOT NULL,
    ID_PRODUTO INT NOT NULL,
    QUANTIDADE INT NOT NULL,
    VALOR_UNITARIO NUMERIC(10,2) NOT NULL,
    VALOR_TOTAL_ITEM NUMERIC(10,2) NOT NULL,
    
    FOREIGN KEY (ID_COMPRA) REFERENCES COMPRA(ID_COMPRA),
    FOREIGN KEY (ID_PRODUTO) REFERENCES PRODUTO(IDPRODUTO),
    PRIMARY KEY(IDITENSCOMPRA)
);
